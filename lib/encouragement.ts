export function getRandomEncouragement(
  isFirstPrayer: boolean = false,
): EncouragementData {
  if (isFirstPrayer) {
    const randomIndex = Math.floor(Math.random() * encouragingMessages.length);
    return encouragingMessages[randomIndex];
  } else {
    const randomIndex = Math.floor(Math.random() * milestoneMessages.length);
    return milestoneMessages[randomIndex];
  }
}

export function getRandomJournalEncouragement(
  isFirstJournal: boolean = false,
): EncouragementData {
  if (isFirstJournal) {
    const randomIndex = Math.floor(
      Math.random() * journalEncouragingMessages.length,
    );
    return journalEncouragingMessages[randomIndex];
  } else {
    const randomIndex = Math.floor(
      Math.random() * journalMilestoneMessages.length,
    );
    return journalMilestoneMessages[randomIndex];
  }
}
